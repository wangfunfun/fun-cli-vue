<template>
  <el-tooltip
    :content="ADMINSYS_LAYOUT_ASIDEOPS.unfold ? t('layout.unfoldActiveTip') : t('layout.unfoldTip')"
    placement="bottom-start"
    effect="light"
    :show-after="1000"
  >
    <div class="unfold" @click="unfoldAside">
      <i class="el-icon-s-fold" :class="{ active: ADMINSYS_LAYOUT_ASIDEOPS.unfold }"></i>
    </div>
  </el-tooltip>
</template>
<script lang="ts">
import { defineComponent, reactive, computed, toRefs } from 'vue'
import { useI18n } from 'vue-i18n'
import { useStore } from '../../../../store/index'

export default defineComponent({
  name: 'unfold',
  setup() {
    const { t } = useI18n()
    const store = useStore()

    const layoutOps = reactive({
      ADMINSYS_LAYOUT_ASIDEOPS: computed(() => store.state.layout.ADMINSYS_LAYOUT_ASIDEOPS)
    })

    // 展开/关闭侧栏
    const unfoldAside = () => {
      store.commit('layout/unfoldAside')
    }

    return {
      ...toRefs(layoutOps),
      unfoldAside,
      t
    }
  }
})
</script>
<style lang="scss" scoped>
@import '@/style/layout.scss';
</style>
